package sample.pojo;

public class Forma {

    private double K;
    private double Y;

    public Forma(double K, double Y){
        this.K = K;
        this.Y = Y;
    }

    public Forma(){}

        public Double getK() {
            return K;
        }

        public void setK(double K) {
            this.K = K;
        }

        public Double getY() {
            return Y;
        }

        public void setY(double Y) {
            this.Y = Y;
        }

}
